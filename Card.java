public class Card {

        private String value;
        private String color;
        private String type;
    
        public Card(String value,String color,String type ) {
            this.value = value;
            this.color = color;
            this.type = type;
        }
    
        public String getValue() {
            return value;
        }
    
        public String getColor() {
            return color;
        }

        public String getType() {
            return type;
        }
    
    }
    
    

